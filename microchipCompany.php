<p style="font-family: sans-serif; font-size: 250%; ">Recruitment Process</p>
<p style="font-size: 130%; margin-bottom: 3%;">The recruitment process of <strong>Microchip</strong> has 3 main rounds:</p>

<ul>
	<li class="round">Candidate profile and resume review</li>

  <ul>
    <li class="round_steps">First off, we would like to eliminate the fear that when you “apply online” your resume ends up in a black hole, never to be seen again.</li>
    <li class="round_steps">Here at Microchip, Hiring managers review all resumes submitted to their specific open positions. Once you have applied to a position, your resume is sent directly to the hiring managers via email.</li>
    <li class="round_steps">Managers will review all resumes, generally within 48 hours of receiving them. Due to the high number of applicants for our positions, managers will only contact and phone screen candidates who are a clear match for the job that is posted.Make sure your resume reflects the skills, education, and years of experience that are required in the posted job description. This may mean you will need to edit your resume for different positions.</li>
    <li class="round_steps">Candidates are generally screened via telephone, by either Human Resources or the Hiring Manager</li>
  </ul>

	<li class="round" style="margin-bottom: 2%;">Interviewing </li>
     <ul>
      <li class="round_steps">Candidates who pass the initial phone screen are invited for a face-to-face interview, either on campus (where the job is located) or via Skype (video conference).</li>
      <li class="round_steps" style="margin-bottom: 2%;">Microchip will make all of the arrangements and travel accommodations (if applicable) for your interview. You will be sent the details via email.</li>
       <li class="round_steps">Interviews can take an entire day</li>
      <li class="round_steps">Confidence matters a lot in this round as the interview panel is going to check whether you are able to manage the stress or not. When you are pushed out of your comfort zone, keep calm and answer the questions that you know.</li>
      <li class="round_steps" style="margin-bottom: 2%;">If you don't know the answer, don't give up, try attempting it with your knowledge./li>
     </ul>

	<li class="round">Job Offers</li>

     <ul>
       <li class="round_steps">Every offer at Microchip is unique so this process can be very different depending on the position you are being offered and what part of the world you are located in.</li>
       <li class="round_steps">We offer competitive salaries and exciting bonus structures to all of our employees; your offer will contain all of these details.</li>
       <li class="round_steps">Once we receive your signed offer back, an HR representative will start preparing for your first day at Microchip.</li>
     </ul>
</ul>

